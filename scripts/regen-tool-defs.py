def parse_dispatch_names(src: str) -> set[str]:
    """Extract tool names from dispatch-registration source text.

    Handles both registration styles in this repo:
      1. `tool_registry!` match arms — `"name" => module::handler,`
      2. handler-map inserts — `m.insert("name", std::sync::Arc::new(handler))`,
         including the rustfmt multi-line form. The `Arc::new` requirement
         keeps serde_json payload inserts inside handler bodies
         (`payload.insert("provenance", json!(..))`) from counting.
    """
    stripped = _strip_line_comments(src)
    names = set(re.findall(r'"([A-Za-z0-9_]+)"\s*=>', stripped))
    names |= set(
        re.findall(
            r'\.insert\(\s*"([A-Za-z0-9_]+)"\s*,\s*(?:std::sync::)?Arc::new',
            stripped,
        )
    )
    return names


def collect_dispatch_names() -> set[str]:
    """Union of dispatch names across all known registration sites."""
    names: set[str] = set()
    for path in DISPATCH_SOURCES:
        names |= parse_dispatch_names(path.read_text())
    return names


def three_way_report(
    dispatch: set[str],
    schema: set[str],
    preset_members: set[str],
    dispatch_only_allowlist: frozenset[str] | set[str] = frozenset(),
) -> dict[str, list[str]]:
    """Classify surface drift between the three registries.

    - dispatch_only: callable but schemaless — invisible to tools/list
    - allowlisted_dispatch_only: sanctioned pending-D3 exceptions
    - pending_d3_symbolic_edit_core: re-list candidates within the allowlist
    - pending_d3_refactor_substrate: substrate-preservation candidates
    - schema_only: listed but not dispatchable — dead schema
    - preset_dead: preset members without a tools.toml definition —
      strings the surface filter can never match
    """
    dispatch_only = dispatch - schema
    allow = set(dispatch_only_allowlist)
    return {
        "dispatch_only": sorted(dispatch_only - allow),
        "allowlisted_dispatch_only": sorted(dispatch_only & allow),
        "pending_d3_symbolic_edit_core": sorted(
            dispatch_only & PENDING_D3_SYMBOLIC_EDIT_CORE
        ),
        "pending_d3_refactor_substrate": sorted(
            dispatch_only & PENDING_D3_REFACTOR_SUBSTRATE
        ),
        "schema_only": sorted(schema - dispatch),
        # Allowlisted (pending-D3) names may legitimately sit in preset
        # constants — callable-but-unlisted on their surfaces.
        "preset_dead": sorted(preset_members - schema - allow),
    }


def lint_description_crossrefs(
    tools: list[dict[str, Any]],
    allowlist: frozenset[tuple[str, str]] | set[tuple[str, str]] = DESCRIPTION_XREF_ALLOWLIST,
) -> list[str]:
    """Flag descriptions that reference other tools by name (spec D7).

    Tool-search surfaces retrieve descriptions independently; chaining
    guidance belongs in suggested_next_tools, not prose name-drops.
    Word-boundary match; self-references are fine.
    """
    names = {tool["name"] for tool in tools}
    offenses: list[str] = []
    for tool in tools:
        desc = tool.get("description", "") or ""
        for other in sorted(names - {tool["name"]}):
            if (tool["name"], other) in allowlist:
                continue
            if re.search(rf"\b{re.escape(other)}\b", desc):
                offenses.append(
                    f"{tool['name']}: description references tool `{other}` "
                    "(move chaining to suggested_next_tools — spec D7)"
                )
    return offenses


def extract_tombstones(src: str) -> set[str]:
    """Names in the `TOMBSTONED_TOOLS: &[(&str, &str)]` const (tools/mod.rs).

    Tombstoned names must never reappear in dispatch, tools.toml, or
    preset constants (Serena `_deleted_tools` pattern).
    """
    m = re.search(
        r"const TOMBSTONED_TOOLS: &\[\(&str, &str\)\] = &\[(.*?)\];",
        src,
        re.DOTALL,
    )
    if not m:
        return set()
    cleaned = _strip_line_comments(m.group(1))
    return set(re.findall(r'\(\s*"([A-Za-z0-9_]+)"\s*,', cleaned))


def enforce_failures(
    report: dict[str, list[str]],
    lint_offenses: list[str],
    tombstone_hits: list[str],
) -> list[str]:
    """Blocking lines for `--enforce-drift`. Allowlisted entries pass."""
    fails: list[str] = []
    for key in ("dispatch_only", "schema_only", "preset_dead"):
        if report[key]:
            fails.append(f"{key} ({len(report[key])}): {', '.join(report[key])}")
    fails.extend(lint_offenses)
    fails.extend(tombstone_hits)
    return fails


def collect_surface_state(
    tools: list[dict[str, Any]],
) -> tuple[dict[str, list[str]], list[str], list[str]]:
    """Live (report, lint_offenses, tombstone_hits) for the working tree."""
    schema_names = {tool["name"] for tool in tools}
    presets_src = PRESETS_RS.read_text()
    preset_members: set[str] = set()
    for const_name in PRESET_TAG_TO_CONST.values():
        preset_members |= extract_preset_const(const_name, presets_src)
    dispatch = collect_dispatch_names()
    report = three_way_report(
        dispatch,
        schema_names,
        preset_members,
        dispatch_only_allowlist=DISPATCH_ONLY_ALLOWLIST,
    )
    lint_offenses = lint_description_crossrefs(tools)
    tombstone_hits: list[str] = []
    tombstones = extract_tombstones(DISPATCH_SOURCES[0].read_text())
    for name in sorted(tombstones):
        if name in dispatch:
            tombstone_hits.append(f"tombstoned tool `{name}` re-introduced in dispatch")
        if name in schema_names:
            tombstone_hits.append(f"tombstoned tool `{name}` re-introduced in tools.toml")
        if name in preset_members:
            tombstone_hits.append(f"tombstoned tool `{name}` re-introduced in presets.rs")
    return report, lint_offenses, tombstone_hits



def validate_preset_tags(tools: list[dict[str, Any]]) -> list[str]:
    """Return human-readable mismatch lines between preset_tags and presets.rs.

    Inverts `[[tool]].preset_tags` into one set per preset tag. The Rust
    side holds the same data as five `pub(crate) const <NAME>: &[&str]`
    arrays in `tool_defs/presets.rs`. The two must agree **for tools
    that exist in tools.toml**. Members listed in presets.rs but absent
    from tools.toml ("orphans" — dead members `tools()` cannot surface)
    are reported by `surface_drift_warnings` as `preset_dead`.
    """
    src = PRESETS_RS.read_text()
    tools_in_toml = {tool["name"] for tool in tools}

    inverse: dict[str, set[str]] = {tag: set() for tag in PRESET_TAG_TO_CONST}
    for tool in tools:
        for tag in tool.get("preset_tags", []) or []:
            if tag in inverse:
                inverse[tag].add(tool["name"])

    mismatches: list[str] = []

    for tag, const_name in PRESET_TAG_TO_CONST.items():
        toml_set = inverse.get(tag, set())
        rs_set = extract_preset_const(const_name, src)
        rs_in_toml = rs_set & tools_in_toml
        only_toml = toml_set - rs_in_toml
        only_rs = rs_in_toml - toml_set
        if only_toml:
            mismatches.append(
                f"{const_name}: in tools.toml preset_tags but not in "
                f"presets.rs: {sorted(only_toml)}"
            )
        if only_rs:
            mismatches.append(
                f"{const_name}: in presets.rs but missing from "
                f"tools.toml preset_tags: {sorted(only_rs)}"
            )

    unknown_tags: set[str] = set()
    for tool in tools:
        for tag in tool.get("preset_tags", []) or []:
            if tag not in PRESET_TAG_TO_CONST:
                unknown_tags.add(tag)
    if unknown_tags:
        mismatches.append(
            f"unknown preset_tags values (not in PRESET_TAG_TO_CONST): "
            f"{sorted(unknown_tags)}"
        )
    return mismatches


def render() -> str:
    with TOOLS_TOML.open("rb") as f:
        data = tomllib.load(f)
    if data.get("schema_version") != SUPPORTED_SCHEMA:
        raise SystemExit(
            f"tools.toml schema_version='{data.get('schema_version')}' is not "
            f"supported by this script ({SUPPORTED_SCHEMA})"
        )
    by_category: dict[str, list[dict[str, Any]]] = {}
    for tool in data.get("tool", []):
        by_category.setdefault(tool["category"], []).append(tool)

    parts: list[str] = [HEADER]
    for category in sorted(by_category):
        parts.append("")
        parts.append(render_category(category, by_category[category]))
    return "\n".join(parts).rstrip() + "\n"


def render_option_match(
    fn_name: str,
    values: dict[str, str],
) -> str:
    lines = [
        f"pub(in crate::tool_defs) fn {fn_name}(name: &str) -> Option<&'static str> {{",
        "    match name {",
    ]
    for name in sorted(values):
        lines.append(f'        "{name}" => Some("{values[name]}"),')
    lines.extend(["        _ => None,", "    }", "}", ""])
    return "\n".join(lines)


def collect_default_listed_tools(tools: list[dict[str, Any]]) -> list[str]:
    """Return `default_visible_rank` tools sorted by rank.

    The default MCP tools/list payload is the expensive first prompt
    surface, so it must be explicit, ordered, and bounded from the same
    declarative source as the rest of the tool metadata.
    """
    ranked: list[tuple[int, str]] = []
    seen_ranks: dict[int, str] = {}
    for tool in tools:
        rank = tool.get("default_visible_rank")
        if rank is None:
            continue
        name = tool["name"]
        if not isinstance(rank, int) or isinstance(rank, bool) or rank <= 0:
            raise SystemExit(
                f"{name}: default_visible_rank must be a positive integer"
            )
        if rank in seen_ranks:
            raise SystemExit(
                f"{name}: default_visible_rank={rank} duplicates "
                f"{seen_ranks[rank]}"
            )
        seen_ranks[rank] = name
        ranked.append((rank, name))

    if not 5 <= len(ranked) <= 9:
        raise SystemExit(
            "default_visible_rank must mark 5-9 tools; "
            f"found {len(ranked)}"
        )
    return [name for _, name in sorted(ranked)]


def render_default_listed_tool_names(names: list[str]) -> str:
    lines = [
        "pub(in crate::tool_defs) fn default_listed_tool_names() -> &'static [&'static str] {",
        "    &[",
    ]
    for name in names:
        lines.append(f'        "{name}",')
    lines.extend(["    ]", "}", ""])
    return "\n".join(lines)


def render_bool_match(fn_name: str, names: list[str]) -> str:
    lines = [
        f"pub(in crate::tool_defs) fn {fn_name}(name: &str) -> bool {{",
        "    matches!(",
        "        name,",
    ]
    for idx, name in enumerate(names):
        prefix = "        " if idx == 0 else "            | "
        lines.append(f'{prefix}"{name}"')
    lines.extend(["    )", "}", ""])
    return "\n".join(lines)


def render_metadata() -> str:
    with TOOLS_TOML.open("rb") as f:
        data = tomllib.load(f)
    if data.get("schema_version") != SUPPORTED_SCHEMA:
        raise SystemExit(
            f"tools.toml schema_version='{data.get('schema_version')}' is not "
            f"supported by this script ({SUPPORTED_SCHEMA})"
        )

    feature_gates: dict[str, str] = {}
    phases: dict[str, str] = {}
    namespaces: dict[str, str] = {}
    annotations: dict[str, str] = {}
    default_listed = collect_default_listed_tools(data.get("tool", []))
    content_mutation_tools = data.get("content_mutation_tools", [])
    symbol_generation_consistent_tools = data.get(
        "symbol_generation_consistent_tools", []
    )
    experimental_tools: dict[str, str] = {}
    for feature, names in data.get("experimental_features", {}).items():
        for name in names:
            experimental_tools[name] = feature

    for tool in data.get("tool", []):
        name = tool["name"]
        if gate := tool.get("feature_gate"):
            feature_gates[name] = gate
        if phase := tool.get("phase"):
            if phase not in VALID_PHASES:
                raise SystemExit(
                    f"{name}: phase='{phase}' is not supported; "
                    f"expected one of {sorted(VALID_PHASES)}"
                )
            phases[name] = phase
        namespace = tool.get("namespace")
        if not namespace:
            raise SystemExit(f"{name}: missing required namespace in tools.toml")
        if namespace not in VALID_NAMESPACES:
            raise SystemExit(
                f"{name}: namespace='{namespace}' is not supported; "
                f"expected one of {sorted(VALID_NAMESPACES)}"
            )
        namespaces[name] = namespace
        annotations[name] = tool.get("annotations", "ro_p")

    parts = [METADATA_HEADER.rstrip(), ""]
    parts.append(render_default_listed_tool_names(default_listed))
    parts.append(render_bool_match("tool_default_listed", default_listed))
    parts.append(render_bool_match("tool_is_content_mutation", content_mutation_tools))
    parts.append(
        render_bool_match(
            "tool_symbol_generation_consistent", symbol_generation_consistent_tools
        )
    )
    parts.append(render_option_match("tool_experimental_feature", experimental_tools))
    parts.append(render_option_match("tool_feature_gate", feature_gates))
    parts.append(render_option_match("tool_phase", phases))
    parts.append(render_option_match("tool_namespace", namespaces))
    parts.append(render_option_match("tool_annotation_key", annotations))
    return "\n".join(parts).rstrip() + "\n"


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    mode = parser.add_mutually_exclusive_group(required=True)
    mode.add_argument("--write", action="store_true", help="rewrite generated files")
    mode.add_argument("--check", action="store_true", help="exit 1 on drift")
    parser.add_argument(
        "--enforce-drift",
        action="store_true",
        help="exit 1 on unexplained surface drift / lint / tombstone hits (#346)",
    )
    args = parser.parse_args()

    with TOOLS_TOML.open("rb") as f:
        toml_data = tomllib.load(f)
    preset_mismatches = validate_preset_tags(toml_data.get("tool", []))
    if preset_mismatches:
        print(
            "preset_tags drift between tools.toml and "
            "crates/codelens-mcp/src/tool_defs/presets.rs:",
            file=sys.stderr,
        )
        for line in preset_mismatches:
            print(f"  - {line}", file=sys.stderr)
        print(
            "edit tools.toml preset_tags or presets.rs constants so they "
            "agree, then re-run.",
            file=sys.stderr,
        )
        return 1

    report, lint_offenses, tombstone_hits = collect_surface_state(
        toml_data.get("tool", [])
    )
    blocking = enforce_failures(report, lint_offenses, tombstone_hits)
    info_lines = list(blocking)
    if report["allowlisted_dispatch_only"]:
        info_lines.append(
            "allowlisted_dispatch_only (pending-D3): "
            + ", ".join(report["allowlisted_dispatch_only"])
        )
        if report["pending_d3_symbolic_edit_core"]:
            info_lines.append(
                "pending_d3_symbolic_edit_core: "
                + ", ".join(report["pending_d3_symbolic_edit_core"])
            )
        if report["pending_d3_refactor_substrate"]:
            info_lines.append(
                "pending_d3_refactor_substrate: "
                + ", ".join(report["pending_d3_refactor_substrate"])
            )
    if info_lines:
        label = "blocking" if (args.enforce_drift and blocking) else "info"
        print(f"surface drift ({label}, #346):", file=sys.stderr)
        for line in info_lines:
            print(f"  - {line}", file=sys.stderr)
    if args.enforce_drift and blocking:
        print(
            "--enforce-drift: unexplained surface drift — register the tool "
            "in tools.toml, tombstone it, or extend the pending-D3 allowlist.",
            file=sys.stderr,
        )
        return 1

    rendered = render()

    if args.write:
        OUT_DIR.mkdir(parents=True, exist_ok=True)
        BUILD_GEN.write_text(rendered)
        METADATA_GEN.write_text(render_metadata())
        # rustfmt the generated file so subsequent `cargo fmt --check` is
        # a no-op. Without this the script emits `Tool::new(\n    ...\n).chain,`
        # but rustfmt rewrites the trailing chain onto a new line, and
        # the next `--check` run sees drift.
        subprocess.run(
            ["cargo", "fmt", "--", str(BUILD_GEN), str(METADATA_GEN)],
            check=True,
            cwd=REPO_ROOT,
        )
        print(f"wrote {BUILD_GEN.relative_to(REPO_ROOT)}")
        print(f"wrote {METADATA_GEN.relative_to(REPO_ROOT)}")
        return 0

    if not BUILD_GEN.exists():
        print(
            f"missing {BUILD_GEN.relative_to(REPO_ROOT)} — run "
            "`python3 scripts/regen-tool-defs.py --write`",
            file=sys.stderr,
        )
        return 1
    # Compare against rustfmt-normalised expected content so drift
    # detection is stable across rustfmt versions.
    fmt = subprocess.run(
        ["rustfmt", "--emit=stdout", "--quiet"],
        input=rendered,
        text=True,
        capture_output=True,
        check=True,
    )
    expected = fmt.stdout
    actual = BUILD_GEN.read_text()
    if actual != expected:
        print(
            f"drift in {BUILD_GEN.relative_to(REPO_ROOT)} — run "
            "`python3 scripts/regen-tool-defs.py --write`",
            file=sys.stderr,
        )
        return 1
    if not METADATA_GEN.exists():
        print(
            f"missing {METADATA_GEN.relative_to(REPO_ROOT)} — run "
            "`python3 scripts/regen-tool-defs.py --write`",
            file=sys.stderr,
        )
        return 1
    metadata_fmt = subprocess.run(
        ["rustfmt", "--emit=stdout", "--quiet"],
        input=render_metadata(),
        text=True,
        capture_output=True,
        check=True,
    )
    metadata_expected = metadata_fmt.stdout
    metadata_actual = METADATA_GEN.read_text()
    if metadata_actual != metadata_expected:
        print(
            f"drift in {METADATA_GEN.relative_to(REPO_ROOT)} — run "
            "`python3 scripts/regen-tool-defs.py --write`",
            file=sys.stderr,
        )
        return 1
    print(f"ok: {BUILD_GEN.relative_to(REPO_ROOT)}")
    print(f"ok: {METADATA_GEN.relative_to(REPO_ROOT)}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
