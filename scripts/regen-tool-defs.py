def bindings_for_category(tools: list[dict[str, Any]]) -> list[str]:
    """Sorted unique annotation bindings used by a category's tools."""
    used = {tool.get("annotations", "ro_p") for tool in tools}
    for ann in used:
        if ann not in ANNOTATION_BINDINGS:
            raise SystemExit(
                f"annotations='{ann}' is not wired into regen-tool-defs.py. "
                "Extend ANNOTATION_BINDINGS first."
            )
    return sorted(used)


def render_tool(tool: dict[str, Any]) -> list[str]:
    name = tool["name"]
    desc = tool["description"]
    schema = tool["input_schema"]
    ann_key = tool.get("annotations", "ro_p")
    ann_var = ANNOTATION_BINDINGS[ann_key]
    schema_json = json.dumps(
        schema, separators=(",", ":"), sort_keys=False, ensure_ascii=False
    )

    builder_chain = []
    if "output_schema" in tool:
        builder_chain.append(f".with_output_schema({tool['output_schema']}())")
    builder_chain.append(f".with_annotations({ann_var}.clone())")
    if "max_response_tokens" in tool:
        builder_chain.append(
            f".with_max_response_tokens({int(tool['max_response_tokens'])})"
        )
    chain = "".join(builder_chain)

    return [
        "        Tool::new(",
        f"            {json.dumps(name, ensure_ascii=False)},",
        f"            {json.dumps(desc, ensure_ascii=False)},",
        f"            json!({schema_json}),",
        f"        ){chain},",
    ]


def category_feature_gate(tools: list[dict[str, Any]]) -> str | None:
    """Return the shared `feature_gate` if every tool agrees, else None.

    The codegen only supports an all-or-nothing feature gate per
    category — the calling convention in `build.rs` extends one Vec
    per category, so a mixed category would need per-Tool cfg
    attributes inside the vec, which the script does not emit. If a
    future PR needs a mixed category, update this function and the
    Tool::new emit path together.
    """
    gates = {tool.get("feature_gate") for tool in tools}
    if len(gates) > 1:
        raise SystemExit(
            f"tools in the same category disagree on feature_gate: {gates}. "
            "Split into separate categories."
        )
    return next(iter(gates))


def render_category(category: str, tools: list[dict[str, Any]]) -> str:
    bindings = bindings_for_category(tools)
    args = ", ".join(f"{ANNOTATION_BINDINGS[b]}: &ToolAnnotations" for b in bindings)
    # `pub` here is intentionally permissive at the function level; the
    # narrow `pub(super) use` re-export in `tool_defs/generated/mod.rs`
    # is what actually constrains visibility to `tool_defs`. See
    # ADR-0013 for the visibility model.
    lines: list[str] = []
    gate = category_feature_gate(tools)
    if gate is not None:
        lines.append(f'#[cfg(feature = "{gate}")]')
    lines.append(f"pub fn {category}_tools({args}) -> Vec<Tool> {{")
    lines.append("    vec![")
    for t in tools:
        lines.extend(render_tool(t))
    lines.append("    ]")
    lines.append("}")
    return "\n".join(lines) + "\n"


def _strip_line_comments(src: str) -> str:
    """Drop `// ...` tails line-wise so quoted names inside comments
    (doc examples, commented-out registrations) never count as matches."""
    cleaned_lines: list[str] = []
    for line in src.split("\n"):
        idx = line.find("//")
        cleaned_lines.append(line if idx < 0 else line[:idx])
    return "\n".join(cleaned_lines)


def extract_preset_const(name: str, src: str) -> set[str]:
    """Return the &str literals inside `pub(crate) const <name>: &[&str] = &[ ... ];`.

    Strips `// ...` line comments from the body before extracting string
    literals — the constants contain prose rationale that would
    otherwise contribute false-positive matches.
    """
    pat = re.compile(
        rf"pub\(crate\) const {re.escape(name)}: &\[&str\] = &\[(.*?)\];",
        re.DOTALL,
    )
    m = pat.search(src)
    if not m:
        raise SystemExit(f"could not locate const {name} in presets.rs")
    cleaned = _strip_line_comments(m.group(1))
    return set(re.findall(r'"([^"]+)"', cleaned))


