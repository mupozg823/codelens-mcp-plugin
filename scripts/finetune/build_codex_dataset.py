#!/usr/bin/env python3
"""Build a Codex-focused fine-tuning dataset from existing CodeLens training corpora.

This builder keeps generic code-search coverage, but explicitly rebalances toward
Codex harness, MCP routing, deferred loading, verifier, and session-aware queries.

Goals:
1. Reduce over-repeated generic queries from the augmented dataset
2. Upweight verified feedback and Codex/MCP/harness task queries
3. Produce a balanced JSONL that is a better default for Codex-focused tuning
"""

from __future__ import annotations

import argparse
import json
import random
import re
from collections import Counter
from dataclasses import dataclass
from pathlib import Path


SCRIPT_DIR = Path(__file__).parent
DEFAULT_BASE = SCRIPT_DIR / "training_pairs_augmented.jsonl"
DEFAULT_FEEDBACK = SCRIPT_DIR / "feedback_pairs_clean.jsonl"
DEFAULT_CSN = SCRIPT_DIR / "training_pairs_csn.jsonl"
DEFAULT_OUTPUT = SCRIPT_DIR / "training_pairs_codex.jsonl"

CODEX_QUERY_KEYWORDS = {
    "codex",
    "harness",
    "mcp",
    "routing",
    "routing_hint",
    "suggested_next_tools",
    "session",
    "client profile",
    "client_name",
    "activate_project",
    "deferred",
    "outputschema",
    "includeoutputschema",
    "surface",
    "budget",
    "preflight",
    "verify_change_readiness",
    "safe_rename_report",
    "refactor preflight",
    "impact/reviewer",
    "onboarding/planning",
}

CODEX_POSITIVE_HINTS = (
    "benchmarks/harness/",
    "crates/codelens-mcp/src/client_profile.rs",
    "crates/codelens-mcp/src/tools/session/",
    "crates/codelens-mcp/src/server/router.rs",
    "crates/codelens-mcp/src/dispatch_response",
    "crates/codelens-mcp/src/protocol.rs",
    "crates/codelens-mcp/src/resource_context.rs",
    "crates/codelens-mcp/src/resources.rs",
)

LOW_SIGNAL_QUERY_MARKERS = (
    "{@inheritdoc}",
    "@generated",
    "auto generated code",
    "<!-- begin-user-doc -->",
    "deepcopyinto is an autogenerated deepcopy",
)


@dataclass(frozen=True)
class Row:
    query: str
    positive: str
    negative: str
    source: str
    codex_focused: bool


def parse_args():
    parser = argparse.ArgumentParser(description="Build a Codex-focused fine-tuning dataset")
    parser.add_argument("--base", default=str(DEFAULT_BASE))
    parser.add_argument("--feedback", default=str(DEFAULT_FEEDBACK))
    parser.add_argument("--csn", default=str(DEFAULT_CSN))
    parser.add_argument("--output", default=str(DEFAULT_OUTPUT))
    parser.add_argument("--stats-json", default="")
    parser.add_argument("--seed", type=int, default=42)
    parser.add_argument("--target-size", type=int, default=1200)
    parser.add_argument("--codex-fraction", type=float, default=0.4)
    parser.add_argument("--max-per-query", type=int, default=2)
    parser.add_argument("--feedback-weight", type=int, default=4)
    parser.add_argument("--codex-weight", type=int, default=3)
    parser.add_argument("--generic-weight", type=int, default=1)
    parser.add_argument("--csn-weight", type=int, default=1)
    return parser.parse_args()


def parse_positive_path(text: str) -> str:
    match = re.search(r" in ([^:]+):", text)
    return match.group(1).strip() if match else ""


def is_codex_focused(query: str, positive: str, negative: str) -> bool:
    query_l = query.lower()
    positive_l = positive.lower()
    negative_l = negative.lower()
    combined = " ".join((query_l, positive_l, negative_l))

    if any(keyword in combined for keyword in CODEX_QUERY_KEYWORDS):
        return True

    positive_path = parse_positive_path(positive)
    if positive_path and any(hint in positive_path for hint in CODEX_POSITIVE_HINTS):
        return True

    return False


def is_low_signal_query(query: str) -> bool:
    query_l = query.strip().lower()
    if not query_l:
        return True
    if query_l in {"reader", "metrics"}:
        return True
    if any(marker in query_l for marker in LOW_SIGNAL_QUERY_MARKERS):
        return True
    if re.match(r"^//\s*set[a-z0-9_]+\s+sets\s+the\s+", query_l):
        return True
    if len(re.sub(r"[^a-z0-9]+", "", query_l)) < 4:
        return True
    return False


def load_rows(path: Path, source: str) -> list[Row]:
    rows: list[Row] = []
    if not path.exists():
        return rows
    with path.open() as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            obj = json.loads(line)
            query = str(obj.get("query", "")).strip()
            positive = str(obj.get("positive", "")).strip()
            negative = str(obj.get("negative", "")).strip()
            if not query or not positive:
                continue
            if source == "csn" and is_low_signal_query(query):
                continue
            rows.append(
                Row(
                    query=query,
                    positive=positive,
                    negative=negative,
                    source=source,
                    codex_focused=is_codex_focused(query, positive, negative),
                )
            )
    return rows


def dedupe_rows(rows: list[Row]) -> list[Row]:
    deduped: list[Row] = []
    seen = set()
    for row in rows:
        key = (row.query, row.positive, row.negative, row.source)
        if key in seen:
            continue
        seen.add(key)
        deduped.append(row)
    return deduped


def enforce_global_query_cap(rows: list[Row], max_per_query: int, rng: random.Random) -> list[Row]:
    capped: list[Row] = []
    counts: Counter[str] = Counter()
    seen = set()
    shuffled = list(rows)
    rng.shuffle(shuffled)
    for row in shuffled:
        key = (row.query, row.positive, row.negative)
        if key in seen:
            continue
        if counts[row.query] >= max_per_query:
            continue
        capped.append(row)
        seen.add(key)
        counts[row.query] += 1
    return capped


def weighted_pool(rows: list[Row], weight: int) -> list[Row]:
    pool: list[Row] = []
    for row in rows:
        pool.extend([row] * max(weight, 1))
    return pool


def pick_rows(pool: list[Row], wanted: int, max_per_query: int, rng: random.Random) -> list[Row]:
    chosen: list[Row] = []
    chosen_keys = set()
    query_counts: Counter[str] = Counter()
    shuffled = list(pool)
    rng.shuffle(shuffled)
    for row in shuffled:
        key = (row.query, row.positive, row.negative)
        if key in chosen_keys:
            continue
        if query_counts[row.query] >= max_per_query:
            continue
        chosen.append(row)
        chosen_keys.add(key)
        query_counts[row.query] += 1
        if len(chosen) >= wanted:
            break
    return chosen


def summarise(rows: list[Row]) -> dict:
    query_counts = Counter(row.query for row in rows)
    source_counts = Counter(row.source for row in rows)
    focused = sum(1 for row in rows if row.codex_focused)
    return {
        "rows": len(rows),
        "unique_queries": len(query_counts),
        "max_query_frequency": max(query_counts.values()) if query_counts else 0,
        "codex_focused_rows": focused,
        "codex_focused_fraction": 0.0 if not rows else round(focused / len(rows), 4),
        "source_counts": dict(sorted(source_counts.items())),
        "top_queries": query_counts.most_common(10),
    }


def main():
    args = parse_args()
    rng = random.Random(args.seed)

    base_rows = dedupe_rows(load_rows(Path(args.base), "base"))
    feedback_rows = dedupe_rows(load_rows(Path(args.feedback), "feedback"))
    csn_rows = dedupe_rows(load_rows(Path(args.csn), "csn"))

    codex_base = [row for row in base_rows if row.codex_focused]
    generic_base = [row for row in base_rows if not row.codex_focused]

    pools = {
        "feedback": weighted_pool(feedback_rows, args.feedback_weight),
        "codex": weighted_pool(codex_base, args.codex_weight),
        "generic": weighted_pool(generic_base, args.generic_weight),
        "csn": weighted_pool(csn_rows, args.csn_weight),
    }

    target_codex = min(
        args.target_size,
        max(len(feedback_rows) * 4, int(args.target_size * args.codex_fraction)),
    )
    target_generic = max(args.target_size - target_codex, 0)

    selected_feedback = pick_rows(
        pools["feedback"], min(target_codex, len(feedback_rows)), args.max_per_query, rng
    )
    remaining_codex = max(target_codex - len(selected_feedback), 0)
    selected_codex = pick_rows(pools["codex"], remaining_codex, args.max_per_query, rng)

    selected_generic = pick_rows(pools["generic"], int(target_generic * 0.7), args.max_per_query, rng)
    selected_csn = pick_rows(pools["csn"], target_generic - len(selected_generic), args.max_per_query, rng)

    merged = dedupe_rows(selected_feedback + selected_codex + selected_generic + selected_csn)
    merged = enforce_global_query_cap(merged, args.max_per_query, rng)
    rng.shuffle(merged)

    output_path = Path(args.output)
    output_path.parent.mkdir(parents=True, exist_ok=True)
    with output_path.open("w") as f:
        for row in merged:
            f.write(
                json.dumps(
                    {
                        "query": row.query,
                        "positive": row.positive,
                        "negative": row.negative,
                        "source": row.source,
                        "codex_focused": row.codex_focused,
                    },
                    ensure_ascii=False,
                )
                + "\n"
            )

    stats = {
        "config": {
            "target_size": args.target_size,
            "codex_fraction": args.codex_fraction,
            "max_per_query": args.max_per_query,
            "feedback_weight": args.feedback_weight,
            "codex_weight": args.codex_weight,
            "generic_weight": args.generic_weight,
            "csn_weight": args.csn_weight,
        },
        "inputs": {
            "base": summarise(base_rows),
            "feedback": summarise(feedback_rows),
            "csn": summarise(csn_rows),
        },
        "output": summarise(merged),
    }

    if args.stats_json:
        stats_path = Path(args.stats_json)
        stats_path.parent.mkdir(parents=True, exist_ok=True)
        stats_path.write_text(json.dumps(stats, ensure_ascii=False, indent=2) + "\n")

    print(json.dumps(stats, ensure_ascii=False, indent=2))
    print(f"\nWrote Codex-focused dataset to {output_path}")


if __name__ == "__main__":
    main()
