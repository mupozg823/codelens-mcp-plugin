#!/usr/bin/env python3
"""Phase P6-b: extreme-efficiency eval harness skeleton.

Reads the 10-scenario battery from
`benchmarks/scenarios/extreme-efficiency.json` (generated by the
typed `EVAL_SCENARIOS` table in `crates/codelens-mcp/src/routing_policy.rs`)
and either:

  --dry-run (default)
        Print the per-scenario plan — scenario id, expected lane, and
        the tool call the eval will execute in the real arm — without
        invoking the MCP server. Useful as a smoke test and as a
        pre-commit sanity check that the fixture and the planner
        agree.

  --live (TODO)
        Spawn `codelens-mcp` via stdio, issue the planned tool call
        per scenario on three arms (native / codelens / hybrid), and
        write `benchmarks/results/extreme-efficiency-<date>.json`.
        This mode is scaffolded but not yet implemented; left as the
        P6-b slice 3 landing target so slice 2 ships a reviewable
        small change.

Exit codes:
    0 — dry-run succeeded / live arm produced a result bundle
    1 — fixture or scenario planner disagreement
    2 — runner error (missing fixture, malformed JSON, etc.)
"""

from __future__ import annotations

import argparse
import datetime
import json
import sys
import time
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parent.parent
SCENARIOS_PATH = REPO_ROOT / "benchmarks" / "scenarios" / "extreme-efficiency.json"
DEFAULT_RESULTS_DIR = REPO_ROOT / "benchmarks" / "results"
DEFAULT_DAEMON_URL = "http://127.0.0.1:7839"

sys.path.insert(0, str(REPO_ROOT / "benchmarks"))
try:
    import benchmark_runtime_common as rc  # noqa: E402
except ImportError:  # pragma: no cover — loader path should always work
    rc = None  # type: ignore[assignment]

# Per-scenario tool call the planner will execute in the "codelens"
# arm. Keyed by scenario `id` so edits to one side are caught by the
# sanity check below. Scenarios that don't yet have a concrete tool
# call are mapped to `None` so dry-run prints "planned: skip".
PLANNED_TOOL_CALLS: dict[str, dict[str, object] | None] = {
    "lookup_single_symbol": {
        "tool": "find_symbol",
        "arguments": {"name": "render_routing_policy_markdown", "exact_match": True},
    },
    "edit_single_line": None,
    "impact_report_multi_file": {
        "tool": "impact_report",
        "arguments": {"path": "crates/codelens-mcp/src/tool_defs/output_schemas.rs"},
    },
    "rename_across_workspace": {
        "tool": "safe_rename_report",
        "arguments": {
            "symbol": "shared_analysis_pool_snapshot",
            "new_name": "shared_analysis_pool_snapshot",
        },
    },
    "onboard_unfamiliar_repo": {
        "tool": "onboard_project",
        "arguments": {},
    },
    "extract_function": None,
    "audit_module_boundary": {
        "tool": "start_analysis_job",
        "arguments": {"job_kind": "module_boundary_report"},
    },
    "dead_code_report_full_repo": {
        "tool": "start_analysis_job",
        "arguments": {"job_kind": "dead_code_report"},
    },
    "trace_call_path": {
        "tool": "trace_request_path",
        "arguments": {"function_name": "main"},
    },
    "fix_single_bug": None,
}


def load_scenarios(path: Path) -> list[dict]:
    if not path.exists():
        print(f"error: scenarios fixture missing at {path}", file=sys.stderr)
        raise SystemExit(2)
    try:
        doc = json.loads(path.read_text(encoding="utf-8"))
    except json.JSONDecodeError as err:
        print(f"error: scenarios fixture is not valid JSON: {err}", file=sys.stderr)
        raise SystemExit(2) from err
    scenarios = doc.get("scenarios")
    if not isinstance(scenarios, list) or len(scenarios) != 10:
        print(
            f"error: expected 10 scenarios, fixture reports {len(scenarios) if isinstance(scenarios, list) else 'n/a'}",
            file=sys.stderr,
        )
        raise SystemExit(2)
    return scenarios


def sanity_check_plan(scenarios: list[dict]) -> int:
    """Ensure PLANNED_TOOL_CALLS covers every fixture id (and no extras).

    Returns 0 on success, 1 on mismatch.
    """
    fixture_ids = {scenario["id"] for scenario in scenarios}
    planner_ids = set(PLANNED_TOOL_CALLS.keys())
    missing = fixture_ids - planner_ids
    extras = planner_ids - fixture_ids
    if missing:
        print(
            f"error: PLANNED_TOOL_CALLS missing entries: {sorted(missing)}",
            file=sys.stderr,
        )
    if extras:
        print(
            f"error: PLANNED_TOOL_CALLS has orphan entries: {sorted(extras)}",
            file=sys.stderr,
        )
    return 1 if missing or extras else 0


def run_dry_run(scenarios: list[dict]) -> int:
    exit_code = sanity_check_plan(scenarios)
    for scenario in scenarios:
        plan = PLANNED_TOOL_CALLS.get(scenario["id"])
        if plan is None:
            label = "skip (native-arm-only, no direct codelens call)"
        else:
            label = f"{plan['tool']}({json.dumps(plan['arguments'], separators=(',', ':'))})"
        print(f"[{scenario['expected_lane']:<27}] {scenario['id']:<32} → {label}")
    return exit_code


def run_live(
    scenarios: list[dict],
    daemon_url: str,
    results_dir: Path,
    project_root: Path | None,
) -> int:
    """Invoke the codelens daemon on each scenario, measure wire bytes +
    latency, write a timestamped result file. Single-arm slice 3 —
    only the `codelens` arm runs; native / hybrid arms follow once a
    baseline exists.
    """
    if rc is None:
        print("error: benchmark_runtime_common import failed", file=sys.stderr)
        return 2

    sanity = sanity_check_plan(scenarios)
    if sanity != 0:
        return sanity

    session_id, _init_payload, _ = rc.initialize_http_session(
        daemon_url, profile="reviewer-graph"
    )
    if not session_id:
        print(
            f"error: initialize returned no session id at {daemon_url}",
            file=sys.stderr,
        )
        return 2

    if project_root is not None:
        activate_payload = rc.extract_tool_payload(
            rc.mcp_http_tool_call(
                daemon_url,
                "activate_project",
                {"project": str(project_root)},
                session_id=session_id,
            )
        )
        data = (
            activate_payload.get("data") if isinstance(activate_payload, dict) else None
        )
        if not (isinstance(data, dict) and data.get("activated")):
            print(
                f"error: activate_project failed for {project_root}: {activate_payload}",
                file=sys.stderr,
            )
            return 2

    measurements: list[dict[str, object]] = []
    for scenario in scenarios:
        plan = PLANNED_TOOL_CALLS.get(scenario["id"])
        if plan is None:
            measurements.append(
                {
                    "scenario_id": scenario["id"],
                    "expected_lane": scenario["expected_lane"],
                    "codelens_arm": {"status": "skipped", "reason": "native-arm-only"},
                }
            )
            continue
        tool = str(plan["tool"])
        arguments = dict(plan["arguments"])
        start = time.monotonic()
        response = rc.mcp_http_tool_call(
            daemon_url, tool, arguments, session_id=session_id
        )
        elapsed_ms = int((time.monotonic() - start) * 1000)
        payload = rc.extract_tool_payload(response)
        serialized = json.dumps(payload, separators=(",", ":"))
        measurements.append(
            {
                "scenario_id": scenario["id"],
                "expected_lane": scenario["expected_lane"],
                "codelens_arm": {
                    "status": "ok" if payload.get("success") is not False else "error",
                    "tool": tool,
                    "bytes": len(serialized),
                    "elapsed_ms": elapsed_ms,
                },
            }
        )

    results_dir.mkdir(parents=True, exist_ok=True)
    today = datetime.date.today().isoformat()
    out_path = results_dir / f"extreme-efficiency-{today}.json"
    doc = {
        "schema_version": 1,
        "generated_at": datetime.datetime.now(datetime.timezone.utc).isoformat(),
        "daemon_url": daemon_url,
        "project_root": str(project_root) if project_root else None,
        "measurements": measurements,
    }
    out_path.write_text(json.dumps(doc, indent=2) + "\n", encoding="utf-8")
    print(f"wrote {out_path.relative_to(REPO_ROOT)}")
    for row in measurements:
        arm = row["codelens_arm"]
        if arm.get("status") == "skipped":
            continue
        print(
            f"  {row['scenario_id']:<32} {arm.get('tool'):<22} "
            f"bytes={arm.get('bytes'):>6} elapsed={arm.get('elapsed_ms'):>5} ms"
        )
    return 0


def main(argv: list[str]) -> int:
    parser = argparse.ArgumentParser(
        description=__doc__, formatter_class=argparse.RawDescriptionHelpFormatter
    )
    mode = parser.add_mutually_exclusive_group()
    mode.add_argument(
        "--dry-run",
        action="store_true",
        default=True,
        help="Print per-scenario plan without invoking MCP (default).",
    )
    mode.add_argument(
        "--live",
        action="store_true",
        help="Invoke codelens daemon (HTTP) per scenario and write results JSON.",
    )
    parser.add_argument(
        "--scenarios",
        type=Path,
        default=SCENARIOS_PATH,
        help=f"Path to scenarios JSON. Defaults to {SCENARIOS_PATH.relative_to(REPO_ROOT)}.",
    )
    parser.add_argument(
        "--daemon-url",
        default=DEFAULT_DAEMON_URL,
        help=f"Codelens daemon URL (default {DEFAULT_DAEMON_URL}).",
    )
    parser.add_argument(
        "--results-dir",
        type=Path,
        default=DEFAULT_RESULTS_DIR,
        help=f"Results directory (default {DEFAULT_RESULTS_DIR.relative_to(REPO_ROOT)}).",
    )
    parser.add_argument(
        "--project-root",
        type=Path,
        default=REPO_ROOT,
        help="Project root to activate before running scenarios (default repo root).",
    )
    args = parser.parse_args(argv)

    scenarios = load_scenarios(args.scenarios)
    if args.live:
        return run_live(scenarios, args.daemon_url, args.results_dir, args.project_root)
    return run_dry_run(scenarios)


if __name__ == "__main__":
    sys.exit(main(sys.argv[1:]))
